package com.truck.model;

import java.util.Date;

public class TruckTransportation {

	int truckId;
	String make;
	String model;
	int year;

	String routeId;
	Date startDate;
	Date endDate;
	String startingPoint;
	String destination;
	Boolean completed;

	public int getTruckId() {
		return truckId;
	}

	public void setTruckId(int truckId) {
		this.truckId = truckId;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public String getRouteId() {
		return routeId;
	}

	public void setRouteId(String routeId) {
		this.routeId = routeId;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getStartingPoint() {
		return startingPoint;
	}

	public void setStartingPoint(String startingPoint) {
		this.startingPoint = startingPoint;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public Boolean getCompleted() {
		return completed;
	}

	public void setCompleted(Boolean completed) {
		this.completed = completed;
	}

	@Override
	public String toString() {
		return "TruckTransportation [truckId=" + truckId + ", make=" + make + ", model=" + model + ", year=" + year
				+ ", routeId=" + routeId + ", startDate=" + startDate + ", endDate=" + endDate + ", startingPoint="
				+ startingPoint + ", destination=" + destination + ", completed=" + completed + "]";
	}
}
